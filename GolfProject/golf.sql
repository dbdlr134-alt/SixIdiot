-- 1. 강사정보 테이블 (TBL_TEACHER_202201)
CREATE TABLE TBL_TEACHER_202201 (
    TEACHER_CODE CHAR(3) NOT NULL PRIMARY KEY,
    CLASS_NAME VARCHAR(12),
    TEACHER_NAME VARCHAR(12),
    -- PDF 명세서의 NUMBER(8)을 INT로 변경 [cite: 8]
    CLASS_PRICE INT, 
    TEACHER_REGIST_DATE CHAR(8)
);

-- 2. 회원정보 테이블 (TBL_MEMBER_202201)
CREATE TABLE TBL_MEMBER_202201 (
    C_NO CHAR(5) NOT NULL PRIMARY KEY,
    -- PDF 명세서의 VARCHAR2를 VARCHAR로 변경 [cite: 12]
    C_NAME VARCHAR(12), 
    PHONE VARCHAR(11),
    ADDRESS VARCHAR(50),
    REGIST_DATE CHAR(8),
    C_TYPE VARCHAR(12)
);

-- 3. 수강신청정보 테이블 (TBL_CLASS_202201)
CREATE TABLE TBL_CLASS_202201 (
    -- 명세서[cite: 17]는 CHAR(8)이나, 샘플 데이터[cite: 19]와 예시[cite: 78]는 '202203' (6자리)이므로 CHAR(6)으로 지정
    REGIST_MONTH CHAR(6), 
    C_NO CHAR(5) NOT NULL,
    CLASS_AREA VARCHAR(15),
    -- PDF 명세서의 NUMBER(8)을 INT로 변경 [cite: 17]
    TUITION INT, 
    TEACHER_CODE CHAR(3),
    -- PDF 명세서에 FK(Foreign Key)로 명시됨 [cite: 17]
    FOREIGN KEY (C_NO) REFERENCES TBL_MEMBER_202201(C_NO),
    FOREIGN KEY (TEACHER_CODE) REFERENCES TBL_TEACHER_202201(TEACHER_CODE)
);

